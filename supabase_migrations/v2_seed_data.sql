-- ── 7. 통계 업데이트 ──────────────────────────────────────────
-- 새 인스턴스들 통계
UPDATE public.event_instances ei SET
  review_count = (SELECT COUNT(*) FROM public.reviews r WHERE r.event_instance_id = ei.id),
  avg_event_rating = (
    SELECT COALESCE(AVG((COALESCE(r.rating_profit,0) + COALESCE(r.rating_traffic,0)) / 2.0), 0)
    FROM public.reviews r WHERE r.event_instance_id = ei.id
      AND (r.rating_profit IS NOT NULL OR r.rating_traffic IS NOT NULL)
  ),
  avg_organizer_rating = (
    SELECT COALESCE(AVG((COALESCE(r.rating_support,0) + COALESCE(r.rating_manners,0)) / 2.0), 0)
    FROM public.reviews r WHERE r.event_instance_id = ei.id
      AND (r.rating_support IS NOT NULL OR r.rating_manners IS NOT NULL)
  )
WHERE ei.id::text LIKE '33333333%';

-- 새 base_events 통계
UPDATE public.base_events be SET
  total_reviews = (
    SELECT COUNT(*) FROM public.reviews r
    JOIN public.event_instances ei ON r.event_instance_id = ei.id
    WHERE ei.base_event_id = be.id
  ),
  avg_event_rating = (
    SELECT COALESCE(AVG((COALESCE(r.rating_profit,0) + COALESCE(r.rating_traffic,0)) / 2.0), 0)
    FROM public.reviews r
    JOIN public.event_instances ei ON r.event_instance_id = ei.id
    WHERE ei.base_event_id = be.id
      AND (r.rating_profit IS NOT NULL OR r.rating_traffic IS NOT NULL)
  ),
  total_instances = (
    SELECT COUNT(*) FROM public.event_instances ei WHERE ei.base_event_id = be.id
  )
WHERE be.id::text LIKE '22222222%';

-- 새 organizers 통계 (기존 + 신규)
UPDATE public.organizers o SET
  total_reviews = (
    SELECT COUNT(*) FROM public.reviews r
    JOIN public.event_instances ei ON r.event_instance_id = ei.id
    WHERE ei.organizer_id = o.id
  ),
  avg_support = (
    SELECT COALESCE(AVG(r.rating_support), 0) FROM public.reviews r
    JOIN public.event_instances ei ON r.event_instance_id = ei.id
    WHERE ei.organizer_id = o.id AND r.rating_support IS NOT NULL
  ),
  avg_manners = (
    SELECT COALESCE(AVG(r.rating_manners), 0) FROM public.reviews r
    JOIN public.event_instances ei ON r.event_instance_id = ei.id
    WHERE ei.organizer_id = o.id AND r.rating_manners IS NOT NULL
  ),
  total_instances = (
    SELECT COUNT(*) FROM public.event_instances ei WHERE ei.organizer_id = o.id
  )
WHERE o.id::text LIKE '11111111%';
